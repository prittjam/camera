function cam = make_ccd(f_mm,ccd_mm,nx,ny)
f = f_mm/ccd_mm*nx;
cc = [nx/2 ny/2]';
K = [f 0 cc(1);
     0 f cc(2);
     0 0 1];
fov = 2*atan2(ccd_mm/2,f_mm);

cam = struct('K', K, ...
             'f', f, ...
             'cc',cc, ...
             'hfov', fov, ...
             'f_mm', f_mm, ...
             'nx',nx, ...
             'ny',ny);
